# app/models/prompt.py
from typing import List


def create_prompt(query: str, documents: List[str]) -> str:
    lines = [
        "请根据以下参考资料回答用户问题。",
        "",
        "用户问题：",
        query,
        "",
        "参考资料：",
    ]

    for index, doc in enumerate(documents, start=1):
        lines.append(f"{index}. {doc}")

    lines.extend(
        [
            "",
            "回答要求：",
            "- 使用中文。",
            "- 语气礼貌、简洁。",
            "- 先安抚用户，再给出可执行建议。",
            "- 不要编造平台规则；如果资料不足，请建议用户通过订单页或官方客服渠道核实。",
            "- 如果用户询问“可以吗”“能不能”“可不可以”等判断类问题，回答开头必须先明确态度，例如“可以申请，但是否通过需平台核实”“不建议”“不可以”或“需要以订单页面和平台核实结果为准”，再说明操作步骤。",
            "- 如果用户询问赔付、索赔、可以赔吗，不要承诺一定赔付；必须明确回答“可以提交售后申请/赔付核实，但是否赔付需平台根据证据和规则判断”，再说明保留证据和订单售后操作。",
            "- 如果参考资料已经列出具体原因、入口或凭证要求，必须优先复述这些关键点，不要只回答“根据页面提示操作”。",
            "- 涉及私下转账时，必须明确“不建议私下转账”，并说明以平台订单结算页和官方渠道为准。",
            "- 涉及优惠券不可用时，必须说明常见限制包括使用门槛、有效期、适用品类、适用商家和支付方式。",
            "- 涉及骑手联系不上时，必须说明先看订单详情页配送状态；仍联系不上或超时时，提交配送异常或未收到餐反馈。",
            "- 涉及餐品撒漏申请售后时，必须说明在订单详情页选择餐品问题、包装破损、撒漏或配送异常，并上传照片凭证。",
            "",
            "客服回答：",
        ]
    )

    return "\n".join(lines)
