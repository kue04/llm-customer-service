FORCE_REPLY_INTENTS = {
    "退款失败",
    "商家拒绝退款",
    "配送异常追问",
    "未收到餐",
    "餐品撒漏售后",
    "食品安全投诉",
    "少送漏送",
    "错送餐品",
    "骑手态度投诉",
    "私下收费风险",
    "验证码诈骗提醒",
}


def apply_primary_intent_rules(primary_context: str) -> str:
    for keywords, rule_reply in INTENT_REPLY_RULES:
        if any(keyword in primary_context for keyword in keywords):
            return rule_reply
    return ""


def apply_primary_intent_rules_with_trace(primary_context: str) -> tuple[str, dict]:
    for index, (keywords, rule_reply) in enumerate(INTENT_REPLY_RULES, start=1):
        matched_keyword = next(
            (keyword for keyword in keywords if keyword in primary_context),
            "",
        )
        if matched_keyword:
            return rule_reply, {
                "matched": True,
                "rule_index": index,
                "matched_keyword": matched_keyword,
                "rule_keywords": list(keywords),
            }
    return "", {"matched": False}


def apply_reply_rules_with_trace(
    query: str,
    reply: str,
    retrieved_items: list[dict],
) -> tuple[str, dict]:
    primary_item = retrieved_items[0] if retrieved_items else {}
    primary_intent = primary_item.get("intent", "")
    primary_context = (
        f"{query} {primary_item.get('category', '')} {primary_intent}"
    )
    primary_rule_reply, rule_trace = apply_primary_intent_rules_with_trace(
        primary_context
    )
    rule_trace.update(
        {
            "primary_category": primary_item.get("category", ""),
            "primary_intent": primary_intent,
            "mode": "force" if primary_intent in FORCE_REPLY_INTENTS else "pass_through",
        }
    )
    if primary_intent == "配送异常追问" and any(word in query for word in ("定位", "位置")) and any(
        word in query for word in ("说到了", "显示到了", "说已到")
    ):
        return (
            "骑手说已到但定位仍很远，属于配送状态和骑手定位不一致。请先在订单详情页核对配送状态，并通过平台内联系功能联系骑手确认实际位置；如果仍无法确认或联系不上，立即提交配送异常或未收到餐反馈，平台会结合骑手位置、配送进度和订单状态核实处理。",
            rule_trace,
        )
    if primary_intent == "未收到餐" and "能退" in query:
        return (
            "骑手取餐后一直没收到餐，可以提交未收到餐反馈；能否退款需要以平台核实结果为准。建议您先确认门口、前台、取餐柜或指定收餐点是否有餐品，并尝试联系骑手；如果仍未找到，请在订单详情页提交未收到餐反馈，平台会核实骑手送达情况和订单状态。",
            rule_trace,
        )
    if primary_rule_reply and primary_intent in FORCE_REPLY_INTENTS:
        return primary_rule_reply, rule_trace
    return reply, rule_trace


def apply_reply_rules(query: str, reply: str, retrieved_items: list[dict]) -> str:
    updated_reply, _ = apply_reply_rules_with_trace(query, reply, retrieved_items)
    return updated_reply
